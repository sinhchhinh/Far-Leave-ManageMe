extension UIView {
    func setViewBorderUIEXTEND ( borderColor : CGColor , cornerRadius : CGFloat , borderWidth : CGFloat ) {
           layer.borderColor = borderColor
           layer.borderWidth = borderWidth
           layer.cornerRadius = cornerRadius
       }
